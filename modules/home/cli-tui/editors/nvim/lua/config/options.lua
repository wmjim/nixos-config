-- 设置
vim.opt.number = true           -- 打开行号
vim.opt.relativenumber = true   -- 打开相对行号
vim.opt.cursorline = true       -- 高亮当前行

-- vim.opt.colorcolumn = "100"     -- 竖线标尺
vim.opt.expandtab = true	    -- tab自动转空格
vim.opt.tabstop = 4		        -- 1制表符对应空格数
vim.opt.shiftwidth = 0       	-- 换行自动缩进与tabstop保持一致

vim.opt.autoread = true         -- 自动加载外部修改

vim.opt.splitbelow = true       -- 默认下方分屏
vim.opt.splitright = true       -- 默认右侧分屏
